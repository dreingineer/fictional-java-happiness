package com.company.mathlib;

public class MathExamples {
    public static void main(String args[]) {
        //absolute
        System.out.println(Math.abs(-24.8));
        //round up
        System.out.println(Math.ceil(14.7));
        //round down
        System.out.println(Math.floor(14.7));
        //max between the two
        System.out.println(Math.max(20, 50));
        //minimum between the two
        System.out.println(Math.min(55, 32));
        //power method eg 2^3
        System.out.println(Math.pow(2, 3));
        //square root
        System.out.println(Math.sqrt(16));
    }
}
